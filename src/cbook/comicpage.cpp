#include "comicpage.h"

/***********************************************************************
 *                             Constructor                             *
 ***********************************************************************/
comicPage::comicPage()
{
   boxes = NULL; 
   totalBoxes = 0;
   next = NULL;
   previous = NULL;
}

/***********************************************************************
 *                              Destructor                             *
 ***********************************************************************/
comicPage::~comicPage()
{
   /* Free All Boxes */
   comicBox* box;
   for(i = 0; i < totalBoxes; i++)
   {
      box = boxes;
      boxes = boxes->next;

      delete(box);
   }
}

/***********************************************************************
 *                               setNext                               *
 ***********************************************************************/
void comicPage::setNext(comicPage* page)
{
   next = page;
}

/***********************************************************************
 *                               getNext                               *
 ***********************************************************************/
comicPage* comicPage::getNext()
{
   return(next);
}

/***********************************************************************
 *                             setPrevious                             *
 ***********************************************************************/
void comicPage::setPrevious(comicPage* page)
{
   previous = page;
}

/***********************************************************************
 *                             getPrevious                             *
 ***********************************************************************/
comicPage* comicPage::getPrevious()
{
   return(previous);
}

/***********************************************************************
 *                              insertBox                              *
 ***********************************************************************/
void comicPage::insertBox(comicBox* box)
{
   if(box)
   {
      if(totalBoxes == 0)
      {
         /* First box on the list */
         box->setNext(box);
         box->setPrevious(box);
         boxes = box;
      }
      else
      {
         /* Put it at the list's end */
         box->setNext(boxes);
         box->setPrevious(boxes->getPrevious());

         box->getNext()->setPrevious(box);
         box->getPrevious()->setNext(box);
      }
      totalBoxes++;
   }
}


