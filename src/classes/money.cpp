
#include "money.h"

/***********************************************************************
 *                               Constructor                           *
 ***********************************************************************/
money::money(): thing()
{
   cost = 0;
}

/***********************************************************************
 *                                Destructor                           *
 ***********************************************************************/
money::~money()
{
}

/***********************************************************************
 *                              removeQuantity                         *
 ***********************************************************************/
bool money::removeQuantity(int qty)
{
   /* Only remove if have the quantity */
   if(cost > qty)
   {
      cost -= qty;
      return(true);
   }
   return(false);
}

/***********************************************************************
 *                              addQuantity                         *
 ***********************************************************************/
void money::addQuantity(int qty)
{
   cost += qty;
}

