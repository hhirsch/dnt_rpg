
#include "money.h"


#define DNT_MONEY_MODEL "models/objetos/itens/money/money.dcc"


/***********************************************************************
 *                               Constructor                           *
 ***********************************************************************/
money::money(): object(DNT_MONEY_MODEL)
{
   cost = 0;
}

/***********************************************************************
 *                                Destructor                           *
 ***********************************************************************/
money::~money()
{
}

/***********************************************************************
 *                                 quantity                            *
 ***********************************************************************/
int money::quantity()
{
   return(cost);
}

/***********************************************************************
 *                              removeQuantity                         *
 ***********************************************************************/
bool money::removeQuantity(int qty)
{
   /* Only remove if have the quantity */
   if(cost > qty)
   {
      cost -= qty;
      setState(cost);
      return(true);
   }
   return(false);
}

/***********************************************************************
 *                              addQuantity                         *
 ***********************************************************************/
void money::addQuantity(int qty)
{
   cost += qty;
   setState(cost);
}

/***********************************************************************
 *                               setQuantity                           *
 ***********************************************************************/
void money::setQuantity(int qty)
{
   cost = qty;
   setState(cost);
}

