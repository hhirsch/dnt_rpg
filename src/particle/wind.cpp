
#include "wind.h"


/*******************************************************************
 *                           Constructor                           *
 *******************************************************************/
wind::wind()
{
}

/*******************************************************************
 *                            Destructor                           *
 *******************************************************************/
wind::~wind()
{
}

/*******************************************************************
 *                           setVelocity                           *
 *******************************************************************/
void wind::setVelocity(GLfloat vX, GLfloat vY, GLfloat vZ)
{
   velX = vX;
   velY = vY;
   velZ = vZ;
}

/*******************************************************************
 *                           getVelocity                           *
 *******************************************************************/
void wind::getVelocity(GLfloat& vX, GLfloat& vY, GLfloat& vZ)
{
   vX = velX;
   vY = velY;
   vZ = velZ;
}


